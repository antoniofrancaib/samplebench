#!/usr/bin/env python3
"""Generate src/data.js for the blind A/B web UI from samplebench.db.

Ships the served models (real generations) and their served sample subset, with
stable sample_ids that join back to metrics/votes. No metrics or model identity
beyond what the UI needs; no fabricated leaderboard numbers.

Run:  python3 bench/pipeline/build_frontend.py   (after build_db.py)
"""
from __future__ import annotations

import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from common import FRONTEND_DATA, FRONTEND_SUITES_V2, connect  # noqa: E402

HEADER = (
    "// AUTO-GENERATED by bench/pipeline/build_frontend.py — do not edit by hand.\n"
    "// Source of truth: ~/lm-bench → bench/db/samplebench.db. Run `make refresh`.\n"
)


def main() -> None:
    con = connect()
    models = []
    families = set()
    rows = con.execute(
        "SELECT model_id, suite_id, label, method, family, algo, nfe "
        "FROM models WHERE is_served = 1 ORDER BY model_id").fetchall()
    for m in rows:
        families.add(m["family"])
        samples = con.execute(
            "SELECT sample_id, text FROM samples "
            "WHERE model_id = ? AND served = 1 ORDER BY sample_id", (m["model_id"],)
        ).fetchall()
        models.append({
            "id": m["model_id"], "name": m["label"], "method": m["method"],
            "family": m["family"], "algo": m["algo"], "nfe": m["nfe"],
            "samples": [{"id": s["sample_id"], "text": s["text"]} for s in samples],
        })
    con.close()

    is_v2 = bool(FRONTEND_SUITES_V2 and any(
        m["id"].startswith("owt_v2_") for m in models
    ))
    payload = (
        HEADER
        + f"export const checkpointFamilies = {json.dumps(sorted(f for f in families if f))};\n"
        + f"export const studyVersion = {json.dumps('v2' if is_v2 else 'v1')};\n"
        + "export const models = "
        + json.dumps(models, ensure_ascii=False, indent=2)
        + ";\n"
    )
    FRONTEND_DATA.write_text(payload, encoding="utf-8")

    n = sum(len(m["samples"]) for m in models)
    print(f"wrote {FRONTEND_DATA.relative_to(FRONTEND_DATA.parents[2])}: "
          f"{len(models)} models, {n} samples")
    for m in models:
        print(f"  {m['id']:26s} {m['method']:16s} {len(m['samples'])} samples")


if __name__ == "__main__":
    main()
