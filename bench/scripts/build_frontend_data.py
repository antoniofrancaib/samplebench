#!/usr/bin/env python3
"""Generate src/data.js for the blind A/B web UI from the curated pool.

Ships only real model generations (paper suite, excluding the human reference).
Carries the stable `sample_id` so collected votes join back to the full metrics
table. No fabricated leaderboard numbers — scores come from real votes later.

Run:  python3 bench/scripts/build_frontend_data.py   (after ingest.py)
"""
from __future__ import annotations

import json
import random
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
from lmbench_common import (  # noqa: E402
    CKPT_SRC, CURATE_SEED, FRONTEND_DATA, FRONTEND_EXCLUDE, FRONTEND_K,
    FRONTEND_SUITES, SAMPLES_DIR, SAMPLES_SRC, iter_model_dirs, method_for,
    read_jsonl,
)

HEADER = (
    "// AUTO-GENERATED by bench/scripts/build_frontend_data.py — do not edit by hand.\n"
    "// Source of truth: ~/lm-bench curated into bench/samples/. Regenerate after ingest.\n"
)


def main() -> None:
    models = []
    families = set()
    for suite in FRONTEND_SUITES:
        for model_id, _ in iter_model_dirs(suite, root=SAMPLES_DIR):
            if model_id in FRONTEND_EXCLUDE:
                continue
            manifest = json.loads((SAMPLES_DIR / suite / model_id / "manifest.json").read_text())
            family = manifest.get("family")
            algo = manifest.get("algo")
            method = method_for(family, algo, manifest.get("source_type"))
            families.add(family)

            rows = list(read_jsonl(SAMPLES_DIR / suite / model_id / "samples.jsonl"))
            rng = random.Random(f"frontend:{CURATE_SEED}:{model_id}")
            k = min(FRONTEND_K, len(rows))
            picked = sorted(rng.sample(rows, k), key=lambda r: r["sample_id"])

            models.append({
                "id": model_id,
                "name": manifest.get("label", model_id),
                "method": method,
                "family": family,
                "algo": algo,
                "nfe": manifest.get("nfe"),
                "samples": [{
                    "id": r["sample_id"],
                    "text": r["text"],
                    # real values where the corpus is scored, else null
                    "genPpl": r.get("raw_generative_ppl") or None,
                    "entropy": r.get("raw_entropy") or None,
                } for r in picked],
            })

    payload = (
        HEADER
        + f"export const checkpointRoot = {json.dumps(str(CKPT_SRC))};\n"
        + f"export const sampleRoot = {json.dumps(str(SAMPLES_SRC))};\n"
        + f"export const checkpointFamilies = {json.dumps(sorted(f for f in families if f))};\n"
        + "export const models = "
        + json.dumps(models, ensure_ascii=False, indent=2)
        + ";\n"
    )
    FRONTEND_DATA.write_text(payload, encoding="utf-8")

    n_samples = sum(len(m["samples"]) for m in models)
    print(f"wrote {FRONTEND_DATA.relative_to(FRONTEND_DATA.parents[2])}: "
          f"{len(models)} models, {n_samples} samples")
    for m in models:
        print(f"  {m['id']:26s} {m['method']:16s} {len(m['samples'])} samples")


if __name__ == "__main__":
    main()
